 * @method static \Illuminate\Database\Eloquent\Builder<static>|Task whereUserId($value)
 * @mixin \Eloquent
 */
class Task extends Model
{
    use HasFactory;

    protected $fillable = [
        'title',
        'is_done',
        'user_id',
    ];

    public function user()
    {
        return $this->belongsTo(User::class);
    }
}